package model;

public class Fabricante {
	private int codFabricante, idFabricante;
	private char dddFabricante;
	private String nomeFabricante, emailFabricante, telefoneFabricante;
	public int getCodFabricante() {
		return codFabricante;
	}
	public void setCodFabricante(int codFabricante) {
		this.codFabricante = codFabricante;
	}
	public int getIdFabricante() {
		return idFabricante;
	}
	public void setIdFabricante(int idFabricante) {
		this.idFabricante = idFabricante;
	}
	public char getDddFabricante() {
		return dddFabricante;
	}
	public void setDddFabricante(char dddFabricante) {
		this.dddFabricante = dddFabricante;
	}
	public String getNomeFabricante() {
		return nomeFabricante;
	}
	public void setNomeFabricante(String nomeFabricante) {
		this.nomeFabricante = nomeFabricante;
	}
	public String getEmailFabricante() {
		return emailFabricante;
	}
	public void setEmailFabricante(String emailFabricante) {
		this.emailFabricante = emailFabricante;
	}
	public String getTelefoneFabricante() {
		return telefoneFabricante;
	}
	public void setTelefoneFabricante(String telefoneFabricante) {
		this.telefoneFabricante = telefoneFabricante;
	}

}
